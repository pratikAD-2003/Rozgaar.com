package com.example.rozgaar.ChatBox;

public class OnScreenModel {
    String roomID,name,profileUri,lastMsg,senderId;

    public String getSenderId() {
        return senderId;
    }

    public void setSenderId(String senderId) {
        this.senderId = senderId;
    }

    public String getRoomID() {
        return roomID;
    }

    public void setRoomID(String roomID) {
        this.roomID = roomID;
    }

    public OnScreenModel() {
    }

    public String getLastMsg() {
        return lastMsg;
    }

    public void setLastMsg(String lastMsg) {
        this.lastMsg = lastMsg;
    }

    public OnScreenModel(String roomID, String name, String profileUri, String lastMsg,String senderId) {
        this.roomID = roomID;
        this.name = name;
        this.profileUri = profileUri;
        this.lastMsg = lastMsg;
        this.senderId = senderId;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getProfileUri() {
        return profileUri;
    }

    public void setProfileUri(String profileUri) {
        this.profileUri = profileUri;
    }
}
